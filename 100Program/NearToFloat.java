public class NearToFloat {
    public static void main(String[] args) {
        // to print ceil and floor value of a double OR Float var
        double num = 3.14;
        System.out.println("The small nearest number ot float is: "+Math.ceil(num));
        System.out.println("The largets number is: "+Math.floor(num));
        // round-off double value 3.14 --> floor = 3.0 AND ceil= 4.0
    }
    
}
