public class PreAndPostUniray {
    public static void main(String[] args) {
        int a=10;
        int b=++a;// b=11,a=11
        System.out.println("For pre value change will be"+a+" and "+b);
        int c=a++; // c=11,a=12
        System.out.println("For post value will be "+a+" and "+c);
        System.out.println(36/7);
        System.out.println(36%7);
    }
}
